// Fonction pour convertir une matrice de bits en tableau d'octets
function convertMatrixToByteArray(matrix: number[]): number[] {
  const byteArray = [];

  for (let i = 0; i < matrix.length; i += 8) {
    let byte = 0;
    for (let j = 0; j < 8; j++) {
      byte <<= 1;
      byte |= matrix[i + j] ? 1 : 0;
    }
    byteArray.push(byte);
  }

  return byteArray;
}

